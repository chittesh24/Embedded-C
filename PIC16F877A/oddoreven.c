#include<pic.h>
void delay(unsigned int x)
{
	while(x--);
}
int main()
{
	TRISB=0x00;
	TRISC=0x00;
	PORTB=0x00;
	PORTC=0x00;
	RC0=0;
	while(1)
	{
		if(RC0==0)
		{
			RB1=1;
			delay(65000);
			RB3=1;
			delay(65000);
			RB5=1;
			delay(65000);
			RB7=1;
			delay(65000);
		}
	    if(RC0==1)
		{
			RB0=1;
			delay(65000);
			RB2=1;
			delay(65000);
			RB4=1;
			delay(65000);
			RB6=1;
			delay(65000);	
		}
	}
}